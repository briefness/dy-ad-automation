def script_to_clip_prompts(
    script: Dict[str, Any],
    product_info: Dict[str, Any],
    preset: Dict[str, Any],
    character_consistency: str = "",
    product_consistency: str = "",
    brand_consistency: str = "",
) -> List[str]:
    """
    将广告脚本转换为视频生成用的 Prompt 列表

    Args:
        script: 广告脚本
        product_info: 产品信息
        preset: 产品预设
        character_consistency: 角色一致性描述
        product_consistency: 产品一致性描述
        brand_consistency: 品牌一致性描述

    Returns:
        Prompt 字符串列表
    """
    name = product_info.get("name", "product")
    character = product_info.get("character", "person")

    prompts = []
    for seg in script["segments"]:
        scene_prompt = seg.get("scene_prompt", "")
        # 注入一致性描述
        full_prompt = (
            f"{scene_prompt}, "
            f"{character}, "
            f"{preset['scene']} scene, "
            f"{preset['lighting']}, "
        )
        if character_consistency:
            full_prompt += f"{character_consistency}, "
        if product_consistency:
            full_prompt += f"{product_consistency}, "
        if brand_consistency:
            full_prompt += f"{brand_consistency}, "

        full_prompt += "9:16 vertical, high quality, cinematic"
        prompts.append(full_prompt)

    return prompts


def script_to_subtitles(
    script: Dict[str, Any],
    clip_duration: int = 5,
    transition_duration: float = 0.5,
    num_clips: Optional[int] = None,
    seg_indices: Optional[List[int]] = None,
    segment_durations: Optional[Dict[int, float]] = None,
) -> List[Dict[str, Any]]:
    """
    将广告脚本转换为字幕列表

    Args:
        script: 广告脚本
        clip_duration: 单片段时长（当 segment_durations 未提供时使用）
        transition_duration: 转场时长（秒），用于修正累积时间偏移
        num_clips: 实际片段数（部分成功时截断多余字幕，None 表示不截断）
        seg_indices: 实际成功的段索引白名单（0-based）。
            提供时按白名单过滤并按合并后顺序重新计算时间轴，
            忽略 num_clips；None 表示不过滤。
        segment_durations: 每段的独立时长映射 {seg_idx: duration}。
            提供时覆盖 clip_duration，实现节奏模板驱动的非等长时间轴。
            None 时使用均匀 clip_duration（向后兼容）。

    Returns:
        字幕列表（text/start/end）
    """
    # P1 #2（v2）：seg_indices 白名单优先；无白名单时退化到 num_clips 截断
    index_set: Optional[set] = set(seg_indices) if seg_indices is not None else None
    # 按白名单顺序建立 seg_idx → 合并位置 的映射
    pos_map: Dict[int, int] = (
        {si: pos for pos, si in enumerate(sorted(seg_indices))}
        if seg_indices is not None
        else {}
    )

    # 构建段时长映射（segment_durations 优先，否则全部用 clip_duration）
    dur_map: Dict[int, float] = {}
    if segment_durations:
        dur_map = dict(segment_durations)
    else:
        # 从 script segments 推断段数，填充统一时长
        for seg in script["segments"]:
            si = seg.get("segment", 0)
            dur_map[si] = float(clip_duration)

    # 计算每段在合并视频中的起始时间（考虑转场重叠和不等长）
    def _get_seg_start(ordered_indices: List[int]) -> Dict[int, float]:
        starts = {}
        current = 0.0
        for i, si in enumerate(ordered_indices):
            if i > 0:
                current -= transition_duration
            starts[si] = current
            current += dur_map.get(si, float(clip_duration))
        return starts

    if index_set is not None:
        ordered = sorted(index_set)
    else:
        ordered = [s.get("segment", i) for i, s in enumerate(script["segments"])]
        if num_clips is not None:
            ordered = ordered[:num_clips]

    seg_starts = _get_seg_start(ordered)

    subtitles = []
    for seg in script["segments"]:
        seg_idx = seg.get("segment", 0)
        if index_set is not None:
            if seg_idx not in index_set:
                continue
        else:
            if num_clips is not None and seg_idx >= num_clips:
                break
        seg_dur = dur_map.get(seg_idx, float(clip_duration))
        seg_start = seg_starts.get(seg_idx, 0.0)
        text = seg.get("subtitle", "")
        start = seg_start + seg_dur * 0.15
        end = seg_start + seg_dur * 0.85
        subtitles.append({"text": text, "start": start, "end": end})
    return subtitles


def script_to_voiceover(
    script: Dict[str, Any],
    clip_duration: int = 5,
    transition_duration: float = 0.5,
    num_clips: Optional[int] = None,
    seg_indices: Optional[List[int]] = None,
    segment_durations: Optional[Dict[int, float]] = None,
    voiceover_style: str = "standard",
) -> List[Dict[str, Any]]:
    """
    将广告脚本转换为口播文案列表

    Args:
        script: 广告脚本
        clip_duration: 单片段时长（当 segment_durations 未提供时使用）
        transition_duration: 转场时长（秒），用于修正累积时间偏移
        num_clips: 实际片段数（部分成功时截断多余口播行，None 表示不截断）
        seg_indices: 实际成功的段索引白名单（0-based）。
            提供时按白名单过滤并按合并后顺序重新计算时间轴，
            忽略 num_clips；None 表示不过滤。
        segment_durations: 每段的独立时长映射 {seg_idx: duration}。
            提供时覆盖 clip_duration，实现节奏模板驱动的非等长时间轴。
            None 时使用均匀 clip_duration（向后兼容）。
        voiceover_style: 口播风格（standard/emotional/energetic/professional/storytelling）。

    Returns:
        口播列表（text/start/end/segment）
    """
    # P1 #2（v2）：seg_indices 白名单优先
    index_set: Optional[set] = set(seg_indices) if seg_indices is not None else None
    pos_map: Dict[int, int] = (
        {si: pos for pos, si in enumerate(sorted(seg_indices))}
        if seg_indices is not None
        else {}
    )

    # 构建段时长映射
    dur_map: Dict[int, float] = {}
    if segment_durations:
        dur_map = dict(segment_durations)
    else:
        for seg in script["segments"]:
            si = seg.get("segment", 0)
            dur_map[si] = float(clip_duration)

    # 计算每段在合并视频中的起始时间
    def _get_seg_start(ordered_indices: List[int]) -> Dict[int, float]:
        starts = {}
        current = 0.0
        for i, si in enumerate(ordered_indices):
            if i > 0:
                current -= transition_duration
            starts[si] = current
            current += dur_map.get(si, float(clip_duration))
        return starts

    if index_set is not None:
        ordered = sorted(index_set)
    else:
        ordered = [s.get("segment", i) for i, s in enumerate(script["segments"])]
        if num_clips is not None:
            ordered = ordered[:num_clips]

    seg_starts = _get_seg_start(ordered)

    def _apply_voiceover_style(text: str, style: str, seg_idx: int) -> str:
        text = text.strip()
        if not text:
            return text
        if style == "energetic":
            return text.rstrip("。.!！") + "！"
        if style == "professional":
            return (
                text.replace("真的", "")
                .replace("赶紧", "现在")
                .replace("宝藏好物", "高品质选择")
                .rstrip("！!")
                .rstrip("。")
                + "。"
            )
        if style == "emotional" and seg_idx == 0 and not text.startswith("你"):
            return "你可能也有这样的感受，" + text
        if style == "storytelling" and seg_idx == 0 and not text.startswith("一开始"):
            return "一开始，" + text
        return text

    lines = []
    for seg in script["segments"]:
        seg_idx = seg.get("segment", 0)
        if index_set is not None:
            if seg_idx not in index_set:
                continue
        else:
            if num_clips is not None and seg_idx >= num_clips:
                break
        seg_dur = dur_map.get(seg_idx, float(clip_duration))
        seg_start = seg_starts.get(seg_idx, 0.0)
        text = _apply_voiceover_style(seg.get("voiceover", ""), voiceover_style, seg_idx)
        start = seg_start + seg_dur * 0.1
        end = seg_start + seg_dur * 0.9
        lines.append({"text": text, "start": start, "end": end, "segment": seg_idx})
    return lines



